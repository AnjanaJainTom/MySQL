-- CREATING DATABASE
CREATE database DISTRIBUTION;
USE DISTRIBUTION;

-- CREATING TABLES
CREATE TABLE Country (
Id INT PRIMARY KEY auto_increment,
Country_name VARCHAR(30),
Population INT,
Area DECIMAL(10,2)
);

CREATE TABLE Persons (
Id INT PRIMARY KEY auto_increment,
Fname VARCHAR(50),
Lname VARCHAR(50),
Population INT,
Rating DECIMAL(3,1),
Country_Id INT,
Country_name VARCHAR(30),
foreign key (Country_Id) references Country(Id)
);

