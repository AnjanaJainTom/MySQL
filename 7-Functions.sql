DELIMITER //
CREATE FUNCTION CALCULATE_AGE(DOB DATE)
RETURNS INT 
DETERMINISTIC 
NO SQL
BEGIN
RETURN DATE_FORMAT(FROM_DAYS(DATEDIFF(CURRENT_DATE,DOB)), '%Y') + 0;
END //
DELIMITER ;

